#include<iostream>
using namespace std;
// main function
int main()
{
	// dynamic initialization of a variable
    int num=1;
	//loop for printing 1 to 100.
	//for loop syntax: for(loop variable; loop condition; loop variable updation formula
    for(int i=0;i<100;i++)
    {
		//printing the value
        cout<<num<<endl;
		//incrementing the value of a variable 
        num++;
    }
}
